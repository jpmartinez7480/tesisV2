read_signal_file<-function(filename){
  read.table(
    filename,
    header = FALSE,
    sep = "\t",
    quote = "\"'",
    dec = ".",
    na.strings = "NA", colClasses = NA, nrows = -1,
    skip = 5, check.names = TRUE,
    as.is = FALSE,
    strip.white = FALSE, fill = TRUE,
    comment.char = "#",
    allowEscapes = FALSE, flush = FALSE,
    stringsAsFactors = default.stringsAsFactors())
}

